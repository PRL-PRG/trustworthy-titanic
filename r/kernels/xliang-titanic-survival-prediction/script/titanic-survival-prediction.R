kagglePred <- function(myfit,test,filename,...){
  mypred <- predict(myfit,test,...)
  myresult <- data.frame(PassengerID = testData$PassengerId,
                        Survived = mypred)
  write.csv(myresult,file=filename,row.names=FALSE)
}


